msig_terms_used <- list()

get_msig_go_bp <- function() {
  if (!requireNamespace("msigdbr", quietly = TRUE)) {
    return(NULL)
  }

  msig_fun <- msigdbr::msigdbr
  msig_args <- names(formals(msig_fun))

  msig <- tryCatch(
    {
      if ("collection" %in% msig_args) {
        msigdbr::msigdbr(
          species = "Mus musculus",
          collection = "C5",
          subcollection = "GO:BP"
        )
      } else {
        msigdbr::msigdbr(
          species = "Mus musculus",
          category = "C5",
          subcategory = "GO:BP"
        )
      }
    },
    error = function(e) {
      warning("Could not load msigdbr GO:BP gene sets: ", conditionMessage(e))
      NULL
    }
  )

  msig
}

build_msig_gene_set <- function(msig, pattern, label) {
  if (is.null(msig)) {
    return(character(0))
  }

  if (!all(c("gs_name", "gene_symbol") %in% colnames(msig))) {
    warning("msigdbr output does not contain expected columns: gs_name and gene_symbol.")
    return(character(0))
  }

  selected_terms <- unique(msig$gs_name[grepl(pattern, msig$gs_name, ignore.case = TRUE)])
  selected_genes <- sort(unique(msig$gene_symbol[msig$gs_name %in% selected_terms]))

  msig_terms_used[[label]] <<- selected_terms
  selected_genes
}

