get_data_layer <- function(obj, assay = "RNA") {
  mat <- tryCatch(
    {
      SeuratObject::LayerData(obj[[assay]], layer = "data")
    },
    error = function(e) NULL
  )

  if (is.null(mat)) {
    mat <- Seurat::GetAssayData(obj, assay = assay, slot = "data")
  }

  mat
}

scale_vector <- function(x) {
  z <- as.numeric(scale(x))
  z[!is.finite(z)] <- 0
  z
}

calculate_mean_expression_score <- function(obj, data_mat, genes, score_name, min_genes = 3) {
  present_genes <- intersect(genes, rownames(data_mat))

  if (length(present_genes) < min_genes) {
    warning(
      "Gene set '", score_name, "' has only ", length(present_genes),
      " genes present in the dataset. Score will be set to NA."
    )
    obj[[score_name]] <- NA_real_
    return(list(obj = obj, present_genes = present_genes, raw_score = rep(NA_real_, ncol(obj))))
  }

  raw_score <- Matrix::colMeans(data_mat[present_genes, colnames(obj), drop = FALSE])
  obj[[score_name]] <- scale_vector(raw_score)

  list(obj = obj, present_genes = present_genes, raw_score = raw_score)
}

